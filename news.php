<h6 class="post-title">Birds of Prey Takes Wing in its Fabulous First Poster</h6>
<p>The Birds of Prey are “heading” out!</p>
<p>
We’ve been hit with so much DC movie news of late, it’s a wonder we’re not all seeing stars. If you are, it’s hopefully a result of our recent casting news and not an actual, physical injury. Still, Harley Quinn’s here to ensure that if there do happen to be any objects orbiting around your head, they’re birds.</p>

<p>Yes, birds. As in Birds of Prey and the Fantabulous Emancipation of One Harley Quinn, the upcoming DC movie in which Margot Robbie returns to the role that made her a DC Extended Universe star. The 2020 film released its first poster today, which features a dazed looking Quinn surrounded by small, winged versions of the movie’s cast. It’s a fun, colorful concept that reminds us that when it comes to getting stuff done, Harley Quinn’s always preferred to wing it.</p>

<div class="responsive-image">
