<h6 class="post-title">How to Binge DC Super Hero Girls in One Metropolis-Sized Marathon</h6>
<p>Green Lantern versus Star Sapphire. Wonder Woman versus Cheetah. Batgirl versus…her part-time job?! The classic DC characters and matchups you’ve always loved from the comics are reimagined with plenty of style and laughs on DC Super Hero Girls! And now that the Cartoon Network series is on Netflix in the US, you can binge to your heart’s content—with a couple of tips from the show’s executive producer, Lauren Faust.</p>
<p>DC Super Hero Girls is an animated show that puts a teenaged twist on some of your favorite DC heroes and villains. It’s got superpowers. It’s got super action. And it’s got some super sass. All together that combines into one fun-filled series that you can watch with the kids for an unforgettable night of family TV.</p>
<div class="responsive-image">
			<img class="center-align"  src="images/imggnews.jpg">
</div>
<p>As the series begins, young Barbara Gordon begrudgingly moves to Metropolis at the request of her father. She’s left behind her entire life and friends in Gotham City, including one Harleen Quinzel. Barbara notices right away that not everything is as it seems at Metropolis High—she is a world-class detective, after all. She follows her instincts and uses her wits to gather together a group of special young women: the shy Karen Beecher (Bumblebee); the peace-loving Jessica Cruz (Green Lantern); the mysterious and magical Zee Zatara (Zatanna); the rebellious Kara Danvers (Supergirl); and the fish-out-of-water Diana Prince (Wonder Woman).</p>


<p>
	Together the girls face their foes and their flaws head-on to become the team that Metropolis needs. (Superman can’t do it all on his own!) While the girls do fly high, that doesn’t mean they’re above having some fun. Every episode comes with a fair bit of snark and attitude—especially from Kara, in a nice nod to her quick temper in the comics—and it’s just the right amount of irreverence and vulnerability that makes the girls feel like real teens with real problems.</p>
<p>
	<div class="responsive-image">
			<img class="center-align"  src="images/imgnews2.jpg">
</div>
<p>Barbara might be brilliant, but she’s also easily distracted, Karen feels small and weak, and Kara hates that her famous cousin gets all the credit she deserves as well. They each need a little help from their friends to learn how to overcome their shortcomings. Oh, and I did I mention the super baddies they have to face at the same time? Battles against Giganta, Livewire, Cheetah and more give us the action-packed heroics that we all love to see from our favorite DC characters, no matter how old they are. Yet, as great as the villains are, the real adversaries the heroes have to face over the course of the series are likely to be much more familiar to school-aged viewers.</p>
<p>
	In a typical example, when Livewire purposely uses social media to embarrass the hapless heroes and their friends, it’s shocking how quickly the crowd turns on the previously-adored crew. Yet, it’s something that any child or teen who’s had experience with online bullying will recognize right away…along with the girls’ initial helplessness at confronting it. A faceless, mocking crowd feels much scarier than a garishly dressed villain with sparking electricity powers. It’s only when the girls make a stand against the bully together that they can face their fears with bravery. Even if you think you can handle whatever life (or Catwoman) throws at you all by yourself, DC Super Hero Girls proves that having your best friends at your back can make all the difference in the world.
</p>
	<div class="responsive-image">
			<img class="center-align"  src="images/imgnews3.jpg">
</div>
<p>
	DC Super Hero Girls mixes humor and slapstick with heart and superpowers to show kids and remind grownups that heroes come in all shapes and sizes. And now that it’s on Netflix, you can binge it all in one powerfully entertaining night! Lauren Faust even stopped by Twitter recently to give followers the scoop on which order to watch the episodes in.  

“Episode 12 should be episode 5,” suggests Faust, “and hold off on episode 9 until after the next batch.”

Let’s hope that next batch of exciting episodes gets here faster than the Flash, because we can’t wait to see what happens to the super friends next.
 

The first thirteen episodes of DC Super Hero Girls are now available to stream on Netflix in the US.

