<ul class="post-meta clearfix no-sep"><li class="post-date"><span class="fa fa-clock-o"></span>September 27, 2019</li><li class="post-author"><span class="fa fa-user"></span><a href="https://mcuexchange.com/author/aeroneclarinal/" rel="author">Aeron Eclarinal</a></li></ul> 
</div>

