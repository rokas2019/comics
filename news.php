<div><img class="center-align" src="images/imgnews.jpg"></div>
<div><p>In an interview with THR, Head of Marvel Studios, Kevin Feige, shared the exciting news.</p>

<p>I am thrilled that Spidey’s journey in the MCU will continue, and I and all of us at Marvel Studios are very excited that we get to keep working on it. Spider-Man is a powerful icon and hero whose story crosses all ages and audiences around the globe. </p>

<p>Feige also mentioned that Spider-Man is the only Marvel hero that would cross “cinematic universes” with him referencing Sony’s own Spidey-verse, as he also teases “surprises” in the future. The release date of the untitled third Spider-Man MCU movie is set on July 16, 2021.</p>

<p>An additional report from Comicbook.com states that the main cast from the first two MCU Spidey flicks will be back for the third movie, which is led by Tom Holland, Zendaya, and Jon Favreau. It is also being reported that Feige will also be producing the film along with Sony Chair, Amy Pascal. Jon Watts is also on board to return to the Director’s chair for the third movie.</p>

<p>Thankfully this means we will get a resolution to that massive cliffhanger ending from Spider-Man Far: From Home, where Peter’s identity is revealed courtesy of J. Jonah Jameson. The report also mentioned that this will not be the last adventure for Tom Holland as Spider-Man. The deal also includes his appearance in an additional MCU film and THR does hint at a new deal for further films being a very real possibility.</p>

<p>This is certainly a welcome development for fans and it signifies that negotiations are still happening behind the scenes. The reference of Sony’s Spidey-verse from Feige is very telling and we can expect that there will be more announcements for the web-slinging hero in the future.</p>
