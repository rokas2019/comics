<h6 class="post-title">'Helstrom' Adds Director Daina Reid</h6>

<p>It has been quite a rollercoaster for Marvel TV today. Right after rumors surface that Kevin Feige wants to establish his own version of Ghost Rider, the Hulu-developed series featuring the return of Gabriel Luna as Robbie Reyes gets passed on. This would’ve kickstarted an additional crossover series for Marvel TV similar to Netflix’s The Defenders and the upcoming animated series The Offenders. It has gotten a bit difficult to keep an overview of all the projects that Marvel TV is working on, especially with Marvel Studios branching out into serialized storytelling with Disney+. Looks like at least one of the announce live-action Hulu series is still moving forward, as according to HN Entertainment Daina Reid will direct some episodes of the upcoming Helstrom series.
</p>
<p>She is joining showrunner Paul Zbyszewsky and stunt coordinator Larry Lam to bring Daimon Helstrom to life on Hulu. This show would’ve joined Ghost Rider as part of their Adventures of Fear crossover but at least this show still seems to be moving forward, as its working title Omens was just recently revealed as well. There is still a chance that Ghost Rider may make an appearance in the show or a future crossover mini-series. Helstrom will take some liberties with the original but it seems to be giving the supernatural franchise a mystery element with Daimon’s father being a serial killer.
</p>
<p>
Australian director Reid has been building up quite a reputation with Hulu. She is Emmy nominated for her work on Hulu’s standout show The Handmaid’s Tale. Her colleague Kari Skogland is also part of the Marvel family, as she is set to direct the six episodes of The Falcon and the Winter Soldier. We might see many more talents from Hulu’s series joining the Marvel fray in other series as the franchise keeps expanding.

