<h6 class="post-title">Jon Watts In Final Negotiations to Return for Third 'Spider-Man'</h6>

<p>What a day for the Marvel Cinematic Universe! After an incredibly rocky few weeks between Marvel Studios and Sony, news broke earlier today that Spider-Man will return to the MCU for at least one more solo movie, completing his well-deserved MCU trilogy. Now, an update to the report from Hollywood Reporter has confirmed that director Jon Watts is in final negotiations to direct the third film! Terms of the deal were not released, but this will make Watts only the second director in the MCU to have his own trilogy, joining James Gunn and the Guardians of the Galaxy saga.
</p>
<p>
Today has been a day of pure joy and celebration with the wall-crawler coming back to complete his solo adventures, especially considering the cliffhanger we were all left with at the end of Spider-Man: Far From Home. With Kevin Feige and Amy Pascal both coming back to produce the film, along with Tom Holland and all of his classmates set to return to New York and finish off their high school careers. This film will be the 4th film of 2021 releasing on July 16th and is now the 7th film of Marvel’s phase 4. Needless to say, the excitement is through the roof as we celebrate the return of our friendly neighborhood MCU Spider-Man!
